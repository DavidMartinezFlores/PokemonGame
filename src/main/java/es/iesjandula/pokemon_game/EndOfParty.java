	/**
	 * Method rotatePokeballThreadOne
	 */
	private void rotatePokeballThreadOne()
	{
		Thread rotatePokebal = new Thread(new Runnable()
		{
			@Override
			public void run()
			{
				int speed = 1;
				while (true)
				{
					for (int i = 0; i < 30; i++)
					{

						EndOfParty.this.jLabel2.setLocation(EndOfParty.this.jLabel2.getX(),
								EndOfParty.this.jLabel2.getY() + speed);
						try
						{
							Thread.sleep(35);
						}
						catch (InterruptedException exception)
						{
							String error = "InterruptedException";
							logger.error(error,exception);
						}
					}
					for (int i = 0; i < 30; i++)
					{

						EndOfParty.this.jLabel2.setLocation(EndOfParty.this.jLabel2.getX(),
								EndOfParty.this.jLabel2.getY() + (speed * -1));
						try
						{
							Thread.sleep(35);
						}
						catch (InterruptedException exception)
						{
							String error = "InterruptedException";
							logger.error(error,exception);
						}
					}
				}
			}
		});
		rotatePokebal.start();
	}

